const updatePlaceholders = (lang) => {
    document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) {
            element.setAttribute('placeholder', translations[lang][key]);
        }
    });
};

const applyTranslations = (lang) => {
    document.querySelectorAll('[data-i18n]').forEach((element) => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });

    updatePlaceholders(lang);
};

const updateLangButton = (langBtn, lang) => {
    if (!langBtn) return;
    langBtn.innerHTML = lang === 'en'
        ? '<span>EN</span> English <i class="fa-solid fa-chevron-down"></i>'
        : '<span>BR</span> Português <i class="fa-solid fa-chevron-down"></i>';
};

export const initTranslations = () => {
    const dropdown = document.querySelector('.dropdown');
    const langBtn = document.getElementById('lang-btn');
    const dropdownItems = document.querySelectorAll('.dropdown-item');

    if (!dropdown || !langBtn || dropdownItems.length === 0) return;

    langBtn.addEventListener('click', (event) => {
        event.stopPropagation();
        dropdown.classList.toggle('open');
    });

    document.addEventListener('click', (event) => {
        if (!dropdown.contains(event.target)) {
            dropdown.classList.remove('open');
        }
    });

    dropdownItems.forEach((item) => {
        item.addEventListener('click', (event) => {
            event.preventDefault();
            dropdownItems.forEach((el) => el.classList.remove('active'));
            item.classList.add('active');

            const lang = item.textContent.includes('English') ? 'en' : 'pt';
            applyTranslations(lang);
            updateLangButton(langBtn, lang);
        });
    });
};
